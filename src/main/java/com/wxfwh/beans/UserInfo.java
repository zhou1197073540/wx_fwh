package com.wxfwh.beans;

public class UserInfo {
    private String avFunds;
    private String uid;
    private String conn;
    private String dailyProfit;
    private String position;
    private String totalFunds;
    private String account;

    public String getAvFunds() {
        return avFunds;
    }

    public void setAvFunds(String avFunds) {
        this.avFunds = avFunds;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }

    public String getConn() {
        return conn;
    }

    public void setConn(String conn) {
        this.conn = conn;
    }

    public String getDailyProfit() {
        return dailyProfit;
    }

    public void setDailyProfit(String dailyProfit) {
        this.dailyProfit = dailyProfit;
    }

    public String getPosition() {
        return position;
    }

    public void setPosition(String position) {
        this.position = position;
    }

    public String getTotalFunds() {
        return totalFunds;
    }

    public void setTotalFunds(String totalFunds) {
        this.totalFunds = totalFunds;
    }

    public String getAccount() {
        return account;
    }

    public void setAccount(String account) {
        this.account = account;
    }
}
